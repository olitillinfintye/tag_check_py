text_file = open("//home/nati//Desktop//check.txt", "r") #load file location here and use // not /
 
#read whole file to a string
data = text_file.read()
tags = re.findall(r'<[^>]+>', data)
for a in tags:
    print(re.sub(r'(<\w+)[^>]+(>)', r'\1\2' , a)) #this line of code select only the tag not the attribute
print("--------------------------------")
for i in tags:
    if i[0] == "<" and i[1] == "/":
        print("valid")
    elif i[0] == "<" and i[1] != "/":
        print("not valid")
#close file
text_file.close()


